import Foundation
import UIKit

extension UIColor {
    static var mainBlueColor: UIColor {
        return UIColor.init(red: 25/255, green: 90/255, blue: 212/255, alpha: 1)
    }
    
    static var darkBlue: UIColor {
        return UIColor.init(red: 19/255, green: 77/255, blue: 178/255, alpha: 1)
    }
    
    static var grayLight: UIColor {
        return UIColor.init(red: 248/255, green: 248/255, blue: 248/255, alpha: 1)
    }
}
